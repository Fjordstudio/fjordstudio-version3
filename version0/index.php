		// set transform-origin for the SVG paths, and lower opacity so that it can be animated
		TweenMax.set(".hex-path", {css:{opacity:.98, transformOrigin:"50% 50%"}});
			
		// stagger animation on page load
		TweenMax.staggerFrom(".hex-path, .visible .hex-path", 1, {css:{scale:.75, rotation:20, opacity:0}, ease:Elastic.easeOut}, .5);

		// hover states
		for(var i = 1; i <= 13; i++){
			
			// find the relevant hexagon
			var hex = document.getElementById('hexagon' + i + '');
			
			// Hover and touch effects
			function moveOver(){
				return TweenMax.to(this, .5, {css:{
					scale:1.1, rotation:-4, opacity:1 }, ease:Elastic.easeOut
				} );
			}
			
			// Cancelling hover and touch effects when focus is moved off the object
			function moveOff(){
				TweenMax.to(this, .2, {css:{
					scale:1, rotation:0, opacity:.98 }, ease:Power0.easeNone
				} );
			}
			
			// Hook the moveOver and moveOff functions to hover and touch events
			hex.addEventListener("mouseenter", moveOver); hex.addEventListener("touchstart", moveOver);
			hex.addEventListener("mouseleave", moveOff); hex.addEventListener("touchend", moveOff);
			
		} // end of for loop
			
